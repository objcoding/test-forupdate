package com.objcoding.transaction;

import org.apache.ibatis.annotations.Mapper;
import org.apache.ibatis.annotations.Param;
import org.apache.ibatis.annotations.Select;
import org.apache.ibatis.annotations.Update;


@Mapper
public interface ForupdateMapper {

	@Select("select * from forupdate where name = #{name} for update")
	Forupdate findByName(@Param("name") String name);

	@Update("update forupdate set version=version + 1 where name = #{name}")
	int updateByName(@Param("name") String name);
}
